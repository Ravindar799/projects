#include<stdio.h>
// monday tuesday wednesday thursday friday saturday sunday
void main()
{
   
    int a;
    printf("please enter your number \n");
    scanf("%d",&a);
    if(a==1){
        printf("monday");
    }
    if(a==2){
        printf("tuesday");
    }
    if(a==3){
        printf("wednesday");
    }
    if(a==4){
        printf("thursday");
    }
    if(a==5){
        printf("friday");
    }
    if(a==6){
        printf("saturday");
    }
    if(a==7){
        printf("sunday");
    }
    
}